import json

def build_merge_prompt(chunk_results: list[dict]) -> str:
    """將多個 Chunk 分析結果整合為最終筆記的 Prompt。"""


    chunk_results_json = json.dumps(
    chunk_results,
    ensure_ascii=False,
    indent=2,
)

    return f"""


請根據以下多個文件分段分析結果，整理成一份完整、適合貼入 Notion 的學習筆記。

請輸出 JSON，且只能輸出 JSON，不可加任何說明文字。

JSON 必須包含以下欄位：

{{
"summary": "完整文件摘要",
"key_points": [
"重點 1",
"重點 2"
],
"mermaid": "Mermaid 圖表內容",
"quiz": [
{{
"question": "題目",
"answer": "答案"
}}
],
"flashcards": [
{{
"front": "正面問題",
"back": "背面答案"
}}
]
}}

規則：

1. 使用繁體中文。
2. 一般文字內容可以適量使用 Emoji。
3. Mermaid 圖表內容禁止使用 Emoji。
4. Mermaid 不可使用 Markdown Code Fence。
5. Mermaid 第一行必須直接以 flowchart TD、flowchart LR、mindmap 或 sequenceDiagram 開始。
6. Mermaid 節點文字需簡短清楚。
7. Quiz 至少產生 5 題，避免重複。
8. Flash Card 至少產生 5 張，避免重複。
9. 不可加入原始內容沒有提到的資訊。
10. 若資訊不足，請保守整理，不可自行捏造。
11. Mermaid 節點文字不可包含條列編號、序號或項目符號。
12. 禁止使用 1.、2.、(1)、（1）、一、二、三、-、* 等作為節點文字開頭。
13. 節點只保留概念名稱或流程名稱，不要照抄原始文件的列表格式。
14. 每個節點文字應控制在 12 個中文字以內。
15. summary 必須完整涵蓋文件的重要區塊，不可只摘要前半段內容。
16. 若文件包含課程目標、課程內容、時數、活動安排、師資、政策背景或限制條件，摘要應盡量涵蓋這些資訊。
17. summary 建議為 250 至 500 個中文字；文件內容較長時可更長，但不可為了簡短而遺漏重要段落。
18. 不可在 summary 中使用「後段被截斷」、「資訊不足」等文字，除非原始文件文字本身確實在句子中斷裂。
19. 產生摘要前，先整合所有 Chunk 的資訊，再撰寫完整摘要。
Mermaid 的主要目的，是呈現文件最核心的知識結構、學習路徑或工作流程。
20. 若文件是課程介紹，Mermaid 必須優先呈現「課程技能學習路徑」，不可將政策背景、補助資訊、結訓活動或行政資訊作為主流程。
21. 政策背景、補助計畫、總時數、師資、就業媒合等資訊，只能作為補充節點，不能取代核心課程內容。
22. 課程類文件應優先呈現：基礎能力 → 核心技術 → 進階 AI 主題 → 專題實作 → 就業成果。
23. Mermaid 必須先判斷文件的主要主題，再決定圖表類型；不可只依照文章出現順序串接內容。
24. 圖表節點之間必須具有明確的學習、因果或流程關係，不可只是把文件段落依序連線。
25. 若核心內容是多個技術主題，請將相近技術整理成同一層級，避免過度分散。
以下是各段分析結果：

{chunk_results_json}
"""
